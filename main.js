// retrive renderProducts function on DOMLoad
window.addEventListener( "DOMContentLoaded", () => {

    renderProducts(productsMenu)
    renderMenuButtons()

} )

// DOM selection
const section = document.querySelector('.section')
const menuButtons = document.getElementById('menu-buttons')

// render menu buttons.
function renderMenuButtons() {

    // get unique menu.
    const names = productsMenu.reduce( (values, item) => {
        if( !values.includes(item.name) ) {
           values.push(item.name)
        }
        return values
    }, ["all"] )

    // create buttons.
    let buttons = names.map( name => {
        return `<button data-id=${name} class="menu__button">
                     ${name}
                </button>`
    } )

    
    buttons = buttons.join('')
    menuButtons.innerHTML = buttons   
    
    let btnsArray = document.querySelectorAll('.menu__button')
    btnsArray.forEach( btn => {
        btn.addEventListener( 'click', e => {
             
            const menu = e.target.dataset.id.toLowerCase()
             
            if( menu === 'all' ) {
                renderProducts(productsMenu)
            }

            else {
                let menuItems = productsMenu.filter( menuItem => {

                     if( menu === menuItem.name ) {
                         return menuItem
                     }

                } )

                renderProducts(menuItems)
            }

        } )
    } )
    
}



// function to render products.
function renderProducts(array) {
    
   let products = array.map( product => {
      
        return `<!-- menu section -->
        <div class="section__product">
          <div class="section__photo-wrapper">
              <img src=${product.img} alt=${product.name} class="section__photo">
          </div>
          
          <h3 class="section__heading">${product.heading}</h3>
          <p class="section__text">${product.description}.</p>
  
          <div class="section__flex">
              <a href="#" class="section__link section__add-to-cart">add to cart</a>
              <a href="#" class="section__link section__price">${product.price}</a>
          </div>
        </div>
        <!-- menu section END-->`;

   } );

   products = products.join('')

   section.innerHTML = products

}



